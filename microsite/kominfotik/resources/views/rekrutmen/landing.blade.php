                <h6 class="text-dark text-sm mb-0">Ketentuan: </h6>
                <ul>
                    <li class="text-sm text-secondary">Seluruh proses pengadaan calon PJLP Suku Dinas Komunikasi, Informatika dan Statistik Kota Administrasi Jakarta Barat Tahun Anggaran 2024, <b>TIDAK DIPUNGUT BIAYA</b>;</li>
                    <li class="text-sm text-secondary">Apabila anggaran tidak tersedia atau tidak mencukupi dari nilai paket ini maka proses penyediaan/pengadaan/perekrutan PJLP menjadi <b>BATAL</b>;</li>
                    <li class="text-sm text-secondary">PJLP terpilih tidak dapat melakukan penuntutan atas pembatalan proses penyediaan/pengadaan/perekrutan PJLP tersebut diatas;</li>
                    <li class="text-sm text-secondary">PJLP terpilih tidak dapat meminta ganti rugi atas pembatalan proses penyediaan/pengadaan/perekrutan PJLP tersebut diatas;</li>
                    <li class="text-sm text-secondary">Pelaksanaan kontrak dilakukan selama 1 (satu) tahun anggaran.</li>
                </ul>
                
                <h6 class="text-dark text-sm mb-0">Persyaratan Umum: </h6>
                <ul>
                    <li class="text-sm text-secondary">Warga Negara Indonesia (WNI);</li>
                    <li class="text-sm text-secondary">Bertakwa kepada Tuhan Yang Maha Esa;</li>
                    <li class="text-sm text-secondary">Setia kepada Pancasila, UUD 1945 dan Negara Kesatuan Republik Indonesia;</li>
                    <li class="text-sm text-secondary">Pria/Wanita, dengan usia minimal 18 (delapan belas) tahun 0 (nol) bulan 0 (nol) hari dan maksimal 56 (lima puluh enam) tahun 0 (nol) bulan 0 (nol) hari per 1 Januari 2024.</li>
                    <li class="text-sm text-secondary">Menguasai Bahasa Indonesia dengan baik dan benar;</li>
                    <li class="text-sm text-secondary">Memahami Bahasa Inggris (pasif);</li>
                    <li class="text-sm text-secondary">Sehat jasmani dan rohani;</li>
                    <li class="text-sm text-secondary">Berkelakuan baik dan tidak pernah dipidana dengan pidana penjara berdasarkan putusan pengadilan yang telah mempunyai kekuatan hukum yang tetap;</li>
                    <li class="text-sm text-secondary">Tidak pernah terlibat penyalahgunaan narkoba;</li>
                    <li class="text-sm text-secondary">Tidak berkedudukan sebagai CPNS/PNS, Calon/Anggota TNI/Polri;</li>
                    <li class="text-sm text-secondary">Tidak berkedudukan sebagai anggota dan/atau pengurus partai politik;</li>
                    <li class="text-sm text-secondary">Tidak pernah diberhentikan dengan hormat tidak atas permintaan sendiri atau tidak dengan hormat sebagai PNS/TNI/Polri atau diberhentikan tidak dengan hormat sebagai pegawai swasta;</li>
                    <li class="text-sm text-secondary">Memiliki integritas, disiplin dan komitmen pada pekerjaan;</li>
                    <li class="text-sm text-secondary">Memiliki wawasan luas, kreativitas tinggi dan mengikuti perkembangan teknologi;</li>
                    <li class="text-sm text-secondary">Mampu dan siap bekerja dalam tekanan deadline bila dibutuhkan/diperlukan serta menyelesaikan pekerjaan sesuai target;</li>
                    <li class="text-sm text-secondary">Siap bekerja di luar jam kerja dan hari libur nasional apabila dibutuhkan/diperlukan;</li>
                    <li class="text-sm text-secondary">Memiliki kemampuan komunikasi yang baik;</li>
                    <li class="text-sm text-secondary">Mampu bekerja secara individu maupun dengan tim;</li>
                    <li class="text-sm text-secondary">Wajib mengenakan pakaian sopan, rapi dan sesuai dengan ketentuan lingkungan kerja;</li>
                    <li class="text-sm text-secondary">Memiliki kemampuan untuk multi-tasking dan mengatur prioritas pekerjaan;</li>
                    <li class="text-sm text-secondary">Diutamakan memiliki pengalaman bekerja di bidang yang sama pada instansi Pemerintah Provinsi DKI Jakarta;</li>
                    <li class="text-sm text-secondary">Mampu membuat laporan administrasi hasil pekerjaan setiap bulan berikut dokumentasi pekerjaan serta daftar hadir kegiatan sesuai dengan ketentuan yang berlaku.</li>
                </ul>

                <h6 class="text-dark text-sm mb-0">Persyaratan Khusus Lainnya: </h6>
                <ul>
                    <li class="text-sm text-secondary">Berdomisili di Jabodetabek yang dibuktikan dengan KTP atau Surat Keterangan Domisili (Diutamakan Jakarta Barat);</li>
                    <li class="text-sm text-secondary">Memiliki NPWP pribadi;</li>
                    <li class="text-sm text-secondary">Memiliki Sertifikat Kompetensi/Training yang dikeluarkan maksimal 2 (dua) tahun terakhir;</li>
                    <li class="text-sm text-secondary">Memiliki kendaraan bermotor bagi posisi Teknisi Jaringan, Fotografer, Reporter;</li>
                    <li class="text-sm text-secondary">Memiliki SIM A/C yang masih berlaku bagi posisi Teknisi Jaringan, Fotografer, Reporter;</li>
                    <li class="text-sm text-secondary">Melampirkan Surat Keterangan Kesehatan terbaru (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Melampirkan Surat Keterangan Tidak Buta Warna bagi posisi Teknisi Jaringan (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Memiliki Surat Keterangan Catatan Kepolisian (SKCK) yang masih berlaku (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Memiliki BPJS Kesehatan dan BPJS Ketenagakerjaan aktif (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Memiliki Surat Keterangan Bebas Narkoba (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Memiliki rekening Bank DKI (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Memiliki akun SIKAP/LPSE dan memiliki NIB (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Surat pernyataan bermaterai tidak menuntut untuk diangkat menjadi Calon Pegawai Negeri Sipil (setelah dinyatakan lulus);</li>
                    <li class="text-sm text-secondary">Tidak terikat dalam hubungan kerja dengan pihak lain;</li>
                    <li class="text-sm text-secondary">Berkomitmen mengikuti proses seleksi sampai dengan dikeluarkannya pengumuman hasil seleksi oleh panitia. </li>
                </ul>

